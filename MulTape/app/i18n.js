export function createTranslator(languageId) {
  const dictionary = DICTIONARIES[languageId] || DICTIONARIES.en;
  return (key, values = {}) => {
    const template = dictionary[key] || DICTIONARIES.en[key] || key;
    return template.replace(/\{(\w+)\}/g, (_match, name) => String(values[name] ?? ""));
  };
}

export function supportedUiLanguage(languageId) {
  return DICTIONARIES[languageId] ? languageId : "en";
}
